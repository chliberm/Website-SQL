            <h3>The process of recycling</h3>
            <p> After the collection of the recyclables, which can be done at different drop off centers, they are sent to a recovery center. There, they are first cleaned so that
            they can be then processed into usable materials that can be used to produce new products. These materials, just like raw materials, are sold to different companies 
            who use the processed recyclables to create their new products. The recycled material have a ever-changing market based on demand and availability. Many items, which 
            you might not be aware of, are made from such recycled content. A few examples of such items include paper towels, different drink containers, news paper, and many more.
            The recycling repeating cycle is not maintained unless consumers buy products the products made from the recycled materials and eventually recycle them as well (if possible.)
            </p>
