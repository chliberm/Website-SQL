            <h3>Why recycle?</h3>
            <p> Among the many benifits of processing certain items and turning them into materials for new ones rather than trash, there are a few key positive outcomes to recycling.
                First and foremost, the regeneration of products from old ones reduces significantly the amount of trash in the landfill, an extremely harmful human ecological footprint.
                In addition to not adding as much waste, recycling also wastes less. Using the raw materials that have already been "harvested" reduces the amount of consumption of natural 
                resources such as timber, water, and minerals. The collection of raw material also results in pollution and significant usage of energy. Therefore, recycling, which reduce 
                the collection of raw materials, reduces also the pollution and energy spent on the collection process. Since a lot goes into the process of recycling, the industry creates 
                many jobs and therefore benefits the economy. 
